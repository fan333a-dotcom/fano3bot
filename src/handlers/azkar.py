async def azkar_handler(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    name = update.effective_user.first_name
    text = update.message.text.strip()

    for category_key, category_list in ALL_CATEGORIES.items():
        if f"أذكار {category_key}" in text.lower() or f"اذكار {category_key}" in text.lower():
            zekr = random.choice(category_list)
            await update.message.reply_text(
                f"🌿 **أذكار {category_key}** 🌿\n\n{zekr}\n\n— {name} 🤲"
            )
            return

    if text.lower() in ("ذكر", "اذكار", "أذكار"):
        await update.message.reply_text(WELCOME_MSG.format(name=name))
        return

    category = random.choice(list(ALL_CATEGORIES.values()))
    zekr = random.choice(category)
    await update.message.reply_text(
        f"🌿 **ذكر عشوائي** 🌿\n\n{zekr}\n\n— {name} 🤲"
    )


def get_azkar_handlers() -> list:
    return [
        MessageHandler(filters.Regex(r"^(ذكر|أذكار|اذكار|أذكار .+|اذكار .+)$"), azkar_handler),
    ]
