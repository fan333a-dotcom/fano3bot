def make_handler(pattern: str, responses: list[str]):
    async def handler(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
        name = update.effective_user.first_name
        await update.message.reply_text(random.choice(responses).format(name=name))

    return MessageHandler(filters.Regex(re.compile(pattern, re.IGNORECASE | re.UNICODE)), handler)


def get_misc_handlers() -> list:
    return [make_handler(pattern, responses) for pattern, responses in response_dict.items()]
